public class Main {
    public static void main(String[] args) {
        for (int count = 0; count <= 30; count++){
            System.out.println(count);
        }
        for (int count = 30; count >= 0; count--) {
            System.out.println(count);
        }
        for (int count = 0; count <= 18; count = count +3) {
            System.out.println(count);
        }
        for (int count = 10; count >= 0; count = count -2) {
            System.out.println(count);
        }
    }
}
